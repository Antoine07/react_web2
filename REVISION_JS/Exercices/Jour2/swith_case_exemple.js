const jour = "jeudi";

switch (jour) {
  case "lundi":
    console.log("Début de la semaine.");
    break; // sort du swith case

  case "mardi":
    console.log("On continue.");
    break;

  case "mercredi":
    console.log("Milieu de semaine.");
    break;

  case "jeudi":
    console.log("On approche du week-end.");
    break;

  case "vendredi":
    console.log("Dernière ligne droite.");
    break;

  default:
    console.log("C’est le week-end !");
}

